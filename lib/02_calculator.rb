def add(num1,num2)
    addition = num1 + num2
    return addition
end

def subtract(num3,num4)
    soust = num3 - num4
    return soust
end

def sum(nums)
    sum = 0
    nums.each {|x| sum+= x}
    return sum
end

def multiply(num1,num2)
    mult = num1 * num2
    return mult
end

def power(num1, num2)
    racine = num1 ** num2
    return racine
end

def factorial(n)
    Math.gamma(n+1)
end
    





